const rowsPerPage = 50;
let currentPage = 1;

function updateTable(page) {
    const tableBody = document.getElementById("tbody");
    //tableBody.innerHTML = "";

    const startIndex = (page - 1) * rowsPerPage;
    const endIndex = startIndex + rowsPerPage;
    for (let i = startIndex; i < endIndex; i++) {
        if (i >= problems.length) break;
        const link = problems[i]['url'];
        const id = problems[i]['id'];
        const contest = problems[i]['contest'];
        const score = problems[i]['score'];
        const row = document.createElement("tr");
        row.innerHTML = `
            <td>${contest}</td>
            <td>${id}</td>
            <td>${score}</td>
            <td><a href="${link}">لینک</a></td>
            <td><input type="checkbox" data-index="${i}" ${
                localStorage.getItem(`checkbox_${i}`) === "true" ? "checked" : ""
            }></td>

        `;
        tableBody.appendChild(row);

        const checkbox = row.querySelector("input[type='checkbox']");
                checkbox.addEventListener("change", function() {
                    const index = this.getAttribute("data-index");
                    localStorage.setItem(`checkbox_${index}`, this.checked);
                });
    }
}

function updatePagination() {
    const pagination = document.getElementById("pagination");
    //pagination.innerHTML = "";

    const numPages = Math.ceil(problems.length / rowsPerPage);
    for (let i = 1; i <= numPages; i++) {
        const li = document.createElement("li");
        li.className = "page-item";
        li.innerHTML = `<a class="page-link" href="#">${i}</a>`;
        li.addEventListener("click", () => {
            currentPage = i;
            updateTable(currentPage);
            updatePagination();
        });
        pagination.appendChild(li);
    }
}

